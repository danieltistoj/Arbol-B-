    private void boton_insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_insertarActionPerformed
         String cadena_valores = JOptionPane.showInputDialog("Ingrese la cantidad de valores que quiere insertar"),numeroAingresar;
         try {
            int num_valores= Integer.parseInt(cadena_valores), contador = 0, dato;
            if(num_valores<=0){
             JOptionPane.showMessageDialog(null,"Ingrese un valor mayor a cero","Advertencia",JOptionPane.WARNING_MESSAGE);   
            }
            else{
                while(contador<num_valores){
                    numeroAingresar = JOptionPane.showInputDialog("Ingrese el valor "+(contador+1));
                    try {
                        dato = Integer.parseInt(numeroAingresar);
                        if(dato<=0){
                           JOptionPane.showMessageDialog(null,"El valor debe de ser mayor a cero","Advertencia",JOptionPane.WARNING_MESSAGE);   
                        }
                        else{
                            arbol.Insertar(dato,arbol.getRaiz());
                            contador++; 
                        }
                       
                    } catch (Exception e) {
                      JOptionPane.showMessageDialog(null,"Llene el campo y solo con valores numericos","Advertencia",JOptionPane.WARNING_MESSAGE);  
                    }
                    
                }
                JOptionPane.showMessageDialog(null,"Valores ingresados con exito","Mensaje",JOptionPane.INFORMATION_MESSAGE); 
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null,"Ingrese solo valores numericos, y no deje el campo vacio","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_boton_insertarActionPerformed
//buscar valores
    private void boton_buscar_valorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_buscar_valorActionPerformed
        String cadena_valor = JOptionPane.showInputDialog("Ingrese el valor que quiere buscar"),numeroAingresar;
        try {
            int valor = Integer.parseInt(cadena_valor), contador = 0, dato;
            if(arbol.Buscar(valor,arbol.getRaiz())){
                JOptionPane.showMessageDialog(null,"El valor "+valor+" existe en el arbol","Mensaje",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                 JOptionPane.showMessageDialog(null,"El valor "+valor+" no existe en el arbol","Mensaje",JOptionPane.INFORMATION_MESSAGE);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Ingrese solo valores numericos, y no deje el campo vacio","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_boton_buscar_valorActionPerformed

