//inicializar el arbol
    private void boto_CrearArbolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boto_CrearArbolActionPerformed
            String cadena_grado = JOptionPane.showInputDialog("Ingrese el grado del arbol"), numeroAingresar; 
            int grado;
            try {
             grado = Integer.parseInt(cadena_grado);
             if(grado<=2){
              JOptionPane.showMessageDialog(null,"El grado debe de ser mayor a 2","Advertencia",JOptionPane.WARNING_MESSAGE); 
             }
             else{
                arbol = new Arbol(grado);
                int ValoresMinimos = (grado-1)/2, contador =0, dato;
                JOptionPane.showMessageDialog(null,"A continuacion ingrese los valores minimos","Mensaje",JOptionPane.INFORMATION_MESSAGE);
                while(contador<ValoresMinimos){
                    numeroAingresar = JOptionPane.showInputDialog("Ingrese el valor "+(contador+1));
                    try {
                        dato = Integer.parseInt(numeroAingresar);
                        if(dato<=0){
                           JOptionPane.showMessageDialog(null,"El valor debe de ser mayor a cero","Advertencia",JOptionPane.WARNING_MESSAGE);   
                        }
                        else{
                            arbol.Insertar(dato,arbol.getRaiz());
                            contador++; 
                        }
                       
                    } catch (Exception e) {
                      JOptionPane.showMessageDialog(null,"Llene el campo y solo con valores numericos","Advertencia",JOptionPane.WARNING_MESSAGE);  
                    }
                }
                
                boton_borrar.setEnabled(true);
                boton_buscar_valor.setEnabled(true);
                boton_imprimir.setEnabled(true);
                boton_insertar.setEnabled(true);
                boton_obtener_altura.setEnabled(true);
                boton_obtener_nivel.setEnabled(true);
               
               JOptionPane.showMessageDialog(null,"Arbol creado con exito","Arbol creado",JOptionPane.INFORMATION_MESSAGE); 
                 
             }
        } catch (Exception e) {
           
           JOptionPane.showMessageDialog(null,"Debe de ingresar solo nunmeros","Advertencia",JOptionPane.WARNING_MESSAGE);

        }
        
        
        
            
    }//GEN-LAST:event_boto_CrearArbolActionPerformed
